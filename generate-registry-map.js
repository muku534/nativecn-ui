const fs = require('fs');
const path = require('path');

const registryDir = path.join(__dirname, 'registry', 'components');
const outputFile = path.join(__dirname, 'registry', 'source-map.ts');

const sourceMap = {};
const metaMap = {};

function kebabToPascal(str) {
  return str
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');
}

if (fs.existsSync(registryDir)) {
  const categories = fs.readdirSync(registryDir, { withFileTypes: true });
  for (const category of categories) {
    if (!category.isDirectory()) continue;

    const categoryDir = path.join(registryDir, category.name);
    const files = fs.readdirSync(categoryDir);

    for (const file of files) {
      const fullPath = path.join(categoryDir, file);
      if (file.endsWith('.tsx') || (file.endsWith('.ts') && !file.endsWith('.d.ts'))) {
        const componentName = file.replace(/\.tsx?$/, '');
        sourceMap[componentName] = fs.readFileSync(fullPath, 'utf8');
      } else if (file.endsWith('.json')) {
        try {
          const raw = fs.readFileSync(fullPath, 'utf8');
          const data = JSON.parse(raw);
          if (data && data.id) {
            metaMap[data.id] = data;
          } else {
            console.warn(`Warning: Metadata file ${file} does not have an "id" field.`);
          }
        } catch (e) {
          console.error(`Error parsing JSON file ${fullPath}:`, e);
        }
      }
    }
  }
}

// Two component ids don't follow the standard kebab-case -> PascalCase
// filename convention, so they're mapped explicitly here.
const ID_TO_SOURCE_KEY_OVERRIDES = {
  "bottom-navigation": "AnimatedBottomNav",
  "bottom-sheet": "DynamicBottomSheet",
};

function resolveSourceKey(id) {
  return ID_TO_SOURCE_KEY_OVERRIDES[id] || kebabToPascal(id);
}

// Verify PascalCase matching between metadata IDs and component filenames
const metaKeys = Object.keys(metaMap);
const sourceKeys = Object.keys(sourceMap);

for (const id of metaKeys) {
  const pascalName = resolveSourceKey(id);
  if (!sourceMap[pascalName]) {
    console.error(`\x1b[31m[ERROR] PascalCase name '${pascalName}' derived from id '${id}' was not found in componentSourceMap!\x1b[0m`);
  }
}

console.log(`Generated componentSourceMap with ${sourceKeys.length} components.`);
console.log(`Generated componentMetaMap with ${metaKeys.length} metadata entries.`);

const content = `// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by generate-registry-map.js to bundle component sources and metadata for Vercel Serverless Functions.
export const componentSourceMap: Record<string, string> = ${JSON.stringify(sourceMap, null, 2)};

export const componentMetaMap: Record<string, any> = ${JSON.stringify(metaMap, null, 2)};
`;

fs.writeFileSync(outputFile, content);
console.log('Successfully written to registry/source-map.ts');
